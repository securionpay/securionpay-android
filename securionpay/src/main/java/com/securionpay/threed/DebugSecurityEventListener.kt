package com.securionpay.threed

import android.util.Log
import com.nsoftware.ipworks3ds.sdk.SecurityEventListener

internal class DebugSecurityEventListener : SecurityEventListener {
    override fun alarm(severity: SecurityEventListener.Severity, event: SecurityEventListener.SecurityEvent) {
        Log.i("DebugEvent", "[Security Alarm] Severity:$severity, Event: $event")
    }
}